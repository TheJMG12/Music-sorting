class Music():
    def _init_(self):
        self.artist=''
        self.genre=''
        self.album=''
        self.most_played=''
        self.year=''

cd = Music()
cd.artist='Travis Scott'
cd.genre='Rap'
cd.album='Rodeo'
cd.most_played='Oh My Dis Side'
cd.year='2015'
print('')
print('Song playing - ', cd.most_played)
print('Artist - ',cd.artist)
print('Album - ',cd.album, 'From', cd.year,)

